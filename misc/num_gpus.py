total_gpus = 0
total_highend_gpus = 0  # A100, H100, B200 only

for line in condor_free_output.splitlines():
    line = line.strip()
    if not line:
        continue
    # Skip headers / separators
    if (line.startswith("NODE") or
        line.startswith("----") or
        line.startswith("MIN") or
        line.startswith("Using ") or
        line.startswith("FREE CORES") or
        line.startswith("FREE GPUS") or
        line.startswith("FREE MEMORY")):
        continue

    parts = line.split()
    # Need at least: NODE PRICE CORES_T CORES_F MEM_T MEM_F GPUS_T GPUS_F MODEL...
    if len(parts) < 8:
        continue

    gpus_total_str = parts[6]  # 7th column is GPUS TOTAL
    if gpus_total_str == "-" or gpus_total_str == "":
        continue

    gpus_total = int(gpus_total_str)
    total_gpus += gpus_total

    # MODEL is everything from column 9 onward
    model = " ".join(parts[8:])
    # Count only A100 / H100 / B200 as "high-end"
    if any(tag in model for tag in ["A100", "H100", "B200"]):
        total_highend_gpus += gpus_total

print("Total GPUs (all models):", total_gpus)
print("Total GPUs (A100 + H100 + B200 only):", total_highend_gpus)
